for(i = 0; i <= 100; i++){
    if(i <= 10){
        console.log(`Voce tem ${i} anos. Voce e crianca.`)
    }else if(i <= 21){
        console.log(`Voce tem ${i} anos. Voce e jovem.`)
    }else if(i <= 50){
        console.log(`Voce tem ${i} anos. Voce e adulto.`)
    }else{
        console.log(`Voce tem ${i} anos. Voce e idoso.`)
    }
}